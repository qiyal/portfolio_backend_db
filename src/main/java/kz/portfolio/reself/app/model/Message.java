package kz.portfolio.reself.app.model;

public class Message {
    private String name;
    private String email;
    private String subject;
    private String text;

    public Message() {}

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getSubject() {
        return subject;
    }

    public String getText() {
        return text;
    }
}
